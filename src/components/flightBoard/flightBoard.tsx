const SIZE = "0.15rem"

interface FlightBoardProps{
    rows: string[],
    sx?: any
}
export default function FlightBoard(props: FlightBoardProps){
    // const words: string[] = [
    //     "abcdefghijklmnopqr",
    //     "stuvwxyz1234567890",
    //     " :-"]

    const convert = (text: string) => {
        const mem = text.split("").map(t => {
            return converter[t]
        })
        return mem.map((letter: number[][]) => {
            return (
                <div style={{display: "flex", flexDirection: "column", marginRight: "0.2rem"}} key={Math.random().toString()}>
                {letter.map((row: number[]) => {
                return (
                    <div style={{display: "flex", flexDirection: "row"}} key={Math.random().toString()}>
                    {row.map((c: number) => {
                        return (<Cell on={c > 0} key={Math.random().toString()}/>)
                    })}
                    </div>
                )})}
                </div>
            )})
    }

    return(
        <div style={{display: "flex", flexDirection: "column", overflowX: "scroll", ...props.sx}}>
            {props.rows.map(w => {
                return (
                    <div style={{display: "flex", flexDirection: "row", marginBottom: "0.5rem"}} key={Math.random().toString()}>
                    {convert(w)}
                    </div>
                )})}
        </div>
    )
}

interface CellProps{
    on?: boolean
}
const Cell = (props: CellProps) => {
    return (
        <div style={{width: SIZE, height: SIZE, borderRadius: "1rem", backgroundColor: props.on ? "white" : "#222222"}}/>
    )
}

